# 첫 번째 줄: 역슬래시(\)와 /, 그리고 역슬래시 2개
#   - 파이썬에서 '\'는 특수문자(이스케이프 문자)라서
#     실제 '\'를 출력하려면 '\\'처럼 두 번 써야 한다.
print("\\    /\\")

# 두 번째 줄: 공백, 괄호, 작은 따옴표 포함 그대로 출력
print(" )  ( ')")

# 세 번째 줄: 괄호 안에 / 출력
print("(  /  )")

# 네 번째 줄: 공백, 역슬래시(\), 괄호, 밑줄, 세로줄
#   - 이 줄도 '\'를 출력하기 위해 '\\' 대신 raw string을 쓰거나
#     그냥 문자열 안에서 '\'를 그대로 적으면 된다.
print(" \\(__)|")